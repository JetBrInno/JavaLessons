package lesson2;

public class Human {
    String name;
    int age;
    String phone;
    String email;
    double rating;

    public Human(String myName, int myAge, String myPhone, String myEmail, double myRating) {
        name = myName;
        age = myAge;
        phone = myPhone;
        email = myEmail;
        rating = myRating;
    }
}
